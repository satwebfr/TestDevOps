package app;

public class Calculator {

    public int sum(int a, int b) {
        return a + b;
    }

    public int minus(int a, int b) {
        return a - b;
    }

    public float divide(int a, int b) {
        return a / b;
    }

    public float multiply(int a, int b) {
        return a * b;
    }

}